<h3>Nieuwsbrief WCS Nederland</h3>

<p>WCS België werkt nauw samen met <a href="https://www.wcs.nl/">WCS Nederland</a>. Het WCS Kenniscentrum Wondzorg Nederland publiceert maandelijks een gratis digitale nieuwsbrief die erop gericht is de kwaliteit van wondzorg op een hoog niveau te houden. De nieuwsbrieven van de voorbije maanden kan u via <a href="https://www.wcs.nl/nieuws/">deze link</a> bekijken.</p>

<p>Door te klikken op <a href="https://www.wcs.nl/over-wcs/inschrijven-wcs-e-brief/">deze link</a>, meldt u zich ook aan voor deze Nederlandse nieuwsbrief. U ontvangt deze dan maandelijks in uw mailbox.</p>

